    public void CreateWord()
    {
        playerWord = "";
        while (randomIndex.Count < 3)
        {
            randNum = UnityEngine.Random.Range(0,words_4.Count());

            if (!randomIndex.Contains(randNum))
            {
                randomIndex.Add(randNum);
            }
        }
        var color = textWord1.color;
        color.a = 1f;
        textWord1.color = color;
        textWord2.color = color;
        textWord3.color = color;
        textWord1.text = words_4[randomIndex[0]].ToUpper();
        textWord2.text = words_4[randomIndex[1]].ToUpper();
        textWord3.text = words_4[randomIndex[2]].ToUpper();
        randomIndex.Clear();
    }
